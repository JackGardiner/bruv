static const f32 CEA_TBL_Pr_midm[400];
f64 cea_Pr_midm(f64 P0_cc, f64 ofr) {
    /* evenly-spaced flattened 2D LUT, access: */
    /*   [i,j] -> [YLEN*i + j] */
    /* max error of: */
    /*   abs 0.382% */
    /*   rel 1.55% */
    const f64 XLO = 1.0;
    const f64 XHI = 5.0;
    const f64 YLO = 1.0;
    const f64 YHI = 3.0;
    enum { XLEN = 10,
           YLEN = 40, };
    f64 x = P0_cc*1e-6;
    f64 y = ofr;
    assert(XLO <= x && x <= XHI, "approximation input oob: x=%g", x);
    assert(YLO <= y && y <= YHI, "approximation input oob: y=%g", y);
    f64 t = (x - XLO) / (XHI - XLO);
    f64 s = (y - YLO) / (YHI - YLO);
    t *= XLEN - 1;
    s *= YLEN - 1;
    i32 i = min(max((i32)t, 0), XLEN - 2);
    i32 j = min(max((i32)s, 0), YLEN - 2);
    t -= i;
    s -= j;
    f64 v00 = CEA_TBL_Pr_midm[YLEN*i + j];
    f64 v01 = CEA_TBL_Pr_midm[YLEN*i + j + 1];
    f64 v10 = CEA_TBL_Pr_midm[YLEN*(i + 1) + j];
    f64 v11 = CEA_TBL_Pr_midm[YLEN*(i + 1) + j + 1];
    f64 v0 = v00 + s*(v01 - v00);
    f64 v1 = v10 + s*(v11 - v10);
    return v0 + t*(v1 - v0);
}

